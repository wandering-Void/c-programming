#include<stdio.h>
int main(void) {

    // Integer data type
    int a = 10;
    printf("Integer: %d\n", a);

    // Float data type
    float b = 5.5;
    printf("Float: %.2f\n", b);

    // Character data type
    char c = 'A';
    printf("Character: %c\n", c);

    // Double data type
    double d = 20.99;
    printf("Double: %.2lf\n", d);

    // String (character array)
    char str[] = "Hello, C!";
    printf("String: %s\n", str);
}